package com.ict.edu;

public class Ex02 {

	// 멤버필드: 상태값, 데이터, 속성, 특징
	// instance 필드	: 객채를 생성할 때 같이 생성되는 필드
	//  static 필드		: 객체 생성과 상관없이 먼저 생성되는 필드
	// final 이 붙음		>>> 상수 
	// final 이 붙지않음 	>>> 변수 
	
	int kor = 80;				// instance 변수
	static int eng = 85;		// static 변수
	final int math = 90;		// instance 상수
	final static int com = 95;	// static 상수	  >>> 일반적으로 상수를 표현할 때 사용
	
}
